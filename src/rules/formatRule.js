export function formatRule(format, value) {
  if (!isDefined(value)) {
    return true;
  }

  if (!FORMATS[format]) {
    throw new Error(`Unknown format "${format}"`);
  }

  return FORMATS[format].test(value);
}

registerRule('format', formatRule, 'is not a valid %{expected}');
